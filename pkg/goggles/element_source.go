type ElementSource struct {
	*baseElement
}

func SOURCE(children ...any) *ElementSource {
	return &ElementSource{
		baseElement: newBaseElement("source", children...),
	}
}

func (e *ElementSource) Add(children ...any) *ElementSource {
	e.baseElement.add(children...)
	return e
}

func (e *ElementSource) Custom(k, v string, dontEscape ...bool) *ElementSource {
	e.baseElement.custom(k, v, dontEscape...)
	return e
}

func (e *ElementSource) BindCustom(k string, v string, dontEscape ...bool) *ElementSource {
	e.baseElement.bindCustom(k, v, dontEscape...)
	return e
}

func (e *ElementSource) appendAttribute(k string, v string, dontEscape ...bool) *ElementSource {
	e.baseElement.appendAttribute(k, v, dontEscape...)
	return e
}

func (e *ElementSource) GenerateVDOM() interface{} {
	return e.baseElement.generateVDOM()
}

